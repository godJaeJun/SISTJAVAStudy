//1*2*3*4*...10
public class Test049
{
	public static void main(String[] args)
	{
		int num=1;
		int dap=1;
		while(num<=10)
		{	
			dap *=num;
			num++;
		}
		System.out.println("연산 결과 : "+dap);
	}
}
